
public class Encapsulation {


		//Encapsulation (Sarmallama, Kapsülleme)
		//Inheritance (Kalıtım)
		//Polymorphism (Çok Biçimlilik)
		//Abstraction (Soyutlaama)
		
		
		public String name,author,publisher;//ad,yazar,yayıncı
		public int numberOfPage;
		
		
		Encapsulation(String name,int numberOfPage,String author,String publisher){
			this.name = name;
			this.numberOfPage = numberOfPage;
			this.author = author;
			this.publisher = publisher;
			
		}
	

}


